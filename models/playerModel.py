class Player():
    def __init__(self, playerId, nameFirst, nameLast, nameFull, position, nflId, college, heightInches, weight, dob, ageAtDraft, playerProfileUrl):
        self.playerId = playerId
        self.nameFirst = nameFirst
        self.nameLast = nameLast
        self.nameFull = nameFull
        self.position = position
        self.nflId = nflId
        self.college = college
        self.heightInches = heightInches
        self.weight = weight
        self.dob = dob
        self.ageAtDraft = ageAtDraft
        self.playerProfileUrl = playerProfileUrl